static uint8_t I2C_data(int data)
{
	TWDR = data;
	TWCR = (1<<TWINT)|(1<<TWEN);
	while (!(TWCR & (1<<TWINT)));
	if ((TWSR & 0xF8) != I2C_DATA_ACK) return 0xff;
	return 0;
}

static uint8_t I2C_start(uint8_t I2C_address, uint8_t toggle_DC)
{
	TWCR = (1<<TWINT)|(1<<TWSTA)|(1<<TWEN);
	while (!(TWCR & (1<<TWINT)));
	if ((TWSR & 0xF8) != I2C_START) return 0xff;

	TWDR = I2C_address;
	TWCR = (1<<TWINT)|(1<<TWEN);
	while (!(TWCR & (1<<TWINT)));
	if ((TWSR & 0xF8) != I2C_MT_SLA_ACK) return 0xff;

	if(toggle_DC) TWDR = 0x40; // DATA SELECTION
	if(!toggle_DC) TWDR = 0x00; // DATA SELECTION
	TWCR = (1<<TWINT)|(1<<TWEN);
	while (!(TWCR & (1<<TWINT)));
	if ((TWSR & 0xF8) != I2C_DATA_ACK) return 0xff;

	return 0;
}

static void I2C_end()
{
	TWCR = (1<<TWINT)|(1<<TWEN)|(1<<TWSTO);
}

static uint8_t I2C_send_command1(int I2C_address, int command)
{
	if(I2C_start(I2C_address, 0)) { spi_address = 0; I2C_end(); return 0xff;}
	if(I2C_data(command)) { spi_address = 0; I2C_end(); return 0xff;}
	I2C_end();
	return 0;
}

static uint8_t I2C_send_command2(int I2C_address, int command1, int command2)
{
	if(I2C_start(I2C_address, 0)) { spi_address = 0; I2C_end(); return 0xff;}
	if(I2C_data(command1)) { spi_address = 0; I2C_end(); return 0xff;}
	if(I2C_data(command2)) { spi_address = 0; I2C_end(); return 0xff;}
	I2C_end();
	return 0;
}


static void LCD_init()
{
	DDRC &= ~((3 << PORTC4));
	TWBR = SPI_SPEED;
}

uint8_t LCD_detect()
{
	uint8_t c = 0;
	LCD_init();
	for(uint8_t a = 1; a < 128; a++)
	{
		if(!I2C_send_command1(a, LCD_CMD_ON))
		{
			spi_address = a;
			for(uint8_t b = 0; b < 17; b++)
			{
				if(pgm_read_byte(CMD_CT + b) == 1) { I2C_send_command1(spi_address, pgm_read_byte(CMD_DT + c)); c++; }
				if(pgm_read_byte(CMD_CT + b) == 2) { I2C_send_command2(spi_address, pgm_read_byte(CMD_DT + c), pgm_read_byte(CMD_DT + c + 1)); c += 2; }
				_delay_us(LCD_DELAY);
			}
			return a;
		}
	}
	return 0;
}

void LCD_set_position(uint8_t X, uint8_t Y)
{
    if(!spi_address) return;
	Y &= 0x07;
	//X += 2; // This is for SH1106 controller
	I2C_send_command1(spi_address, LCD_SET_ROW | Y);
	_delay_us(LCD_DELAY);
	I2C_send_command1(spi_address, LCD_SET_COLH | ((X >> 4) & 0x0f));
	_delay_us(LCD_DELAY);
	I2C_send_command1(spi_address, LCD_SET_COLL | (X & 0x0f));
	_delay_us(LCD_DELAY);
	lcd_Y = Y;
}

void LCD_clear_line(uint8_t line)
{
    if(!spi_address) return;
	uint8_t b = 0;
	LCD_set_position(0,line);
	if(I2C_start(spi_address, 1)) {I2C_end(); return;}
	for(b = 0; b < 128; b++)
	{
		if(I2C_data(0x00)) {I2C_end(); return;}
	}
	I2C_end();
	_delay_us(LCD_DELAY);
	LCD_set_position(0,line);
}

void LCD_clear_screen()
{
    if(!spi_address) return;
	uint8_t a = 0;

	for(a = 0; a < 8; a++)
	{
		LCD_clear_line(a);
	}
	LCD_set_position(0,0);
}

void LCD_clear_offset(uint8_t line, uint8_t start, uint8_t offset)
{
    if(!spi_address) return;
	uint8_t b = 0;
	if(start + offset > 127) return;
	LCD_set_position(start,line);
	if(I2C_start(spi_address, 1)) {I2C_end(); return;}
	for(b = start; b < (start + offset); b++)
	{
		if(I2C_data(0x00)) {I2C_end(); return;}
	}
	I2C_end();
	_delay_us(LCD_DELAY);
	LCD_set_position(start,line);
}

void LCD_data(uint8_t data)
{
	if(I2C_start(spi_address, 1)) { I2C_end(); return; }
	if(I2C_data(data)) { I2C_end(); return; }
	I2C_end();
	_delay_us(LCD_DELAY);
}

void LCD_char(uint8_t character)
{
    if(!spi_address) return;
	uint8_t a = 0;

	if(character == '\n')
	{
		lcd_Y++;
		if(lcd_Y > 7) lcd_Y = 0;
		LCD_set_position(0,lcd_Y);
		return;
	}

	if ((character < 0x20) && (character > 0x7e)) return;

	if(I2C_start(spi_address, 1)) {I2C_end(); return;}
	for(a = 0; a < 5; a++)
	{
		if(I2C_data(pgm_read_byte(&CHARACTERS[character - 32][a]))) {I2C_end(); return;}
	}
	if(I2C_data(0x00)) {I2C_end(); return;}
	I2C_end();
	_delay_us(LCD_DELAY);
}

void LCD_string(char * data)
{
    if(!spi_address) return;
	uint8_t a = 0;
	while(data[a] != '\0')
	{
		if((data[a] == '\0') || (data[a] == '\r')) return;
		LCD_char((uint8_t)data[a++]);
	}
}

void LCD_dec(uint16_t data)
{
    if(!spi_address) return;
	char data_str[32];
    itoa(data, (char *)data_str, 10);
	LCD_string(data_str);
}

void LCD_HEX8(uint8_t data, uint8_t toggle0x)
{
	char a = 0;
	char b = 0;

	a = (data >> 4) & 0x0f;
	if(a > 9) a += (0x41 - 10);
	if(a < 10) a += 0x30;
	b = data & 0x0f;
	if(b > 9) b += (0x41 - 10);
	if(b < 10) b += 0x30;

    if(toggle0x == 1) LCD_string("0x");
    LCD_char(a);
    LCD_char(b);
    
}