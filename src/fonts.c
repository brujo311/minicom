uint8_t get_char_size(uint8_t character, uint8_t font_number, uint8_t * size_x, uint8_t * size_y)
{
	if ((character < 0x20) && (character > 0x7f)) return 0; // Char out of range

    character -= 32;

    if(font_number == 1)
    {
        size_x[0] = 6; // Char size X
        size_y[0] = 8; // Char size Y
        return 2;
	}

    if(font_number == 2)
    {
        size_x[0] = 12; // Char size X
        size_y[0] = 16; // Char size Y
        return 2;
    }

    if(font_number == 3)
    {
        size_x[0] = 10; // Char size X
        size_y[0] = 16; // Char size Y
        return 2;
    }

    return 0;
}

uint8_t get_char(uint8_t character, uint8_t * destination, uint8_t font_number)
{
    uint8_t a = 0;

    if ((character < 0x20) && (character > 0x7f)) return a; // Char out of range

    character -= 32;

    if(font_number == 1)
    {
        destination[0] = 5; // Char size X
        destination[1] = 1; // Char size Y (in bytes)
        destination[2] = 1; // Dummies
        for(a = 3; a < 8; a++)
            destination[a] = pgm_read_byte(&charset_1[(character * 5) + a - 3]);
        return a + 1;
	}

    if(font_number == 2)
    {
        destination[0] = 10; // Char size X
        destination[1] = 2; // Char size Y (in bytes)
        destination[2] = 2; // Dummies
        for(a = 0; a < 10; a++)
        {
            destination[a + 13] = pgm_read_byte(&charset_2[(character * 20) + (a * 2)]);
            destination[a + 3] = pgm_read_byte(&charset_2[(character * 20) + (a * 2) + 1]);
        }
        return a + 1;
    }

    if(font_number == 3)
    {
        destination[0] = 10; // Char size X
        destination[1] = 2; // Char size Y (in bytes)
        destination[2] = 0; // Dummies
        for(a = 0; a < 10; a++)
        {
            destination[a + 3] = pgm_read_byte(&charset_3[(character * 20) + (a * 2)]);
            destination[a + 13] = pgm_read_byte(&charset_3[(character * 20) + (a * 2) + 1]);
        }
        return a + 1;
    }

    return a;
}

